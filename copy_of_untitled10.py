import numpy as np
import cv2
from tensorflow.keras.models import load_model
import matplotlib.pyplot as plt

# Load model
model = load_model("hand_sign_model.h5")

labels = ["✊ Fist", "✌️ Peace", "👎 Thumbs Down", "👍 Thumbs Up"]

# Load image
img = cv2.imread('/content/test1.jpeg')

if img is None:
    print("Image not loaded!")
else:
    img_resized = cv2.resize(img, (224,224))
    img_norm = img_resized / 255.0
    img_input = np.reshape(img_norm, (1,224,224,3))

    pred = model.predict(img_input)
    class_id = np.argmax(pred)
    confidence = np.max(pred) * 100

    print("Prediction:", labels[class_id])
    print("Confidence:", confidence, "%")

    # Show image
    plt.imshow(cv2.cvtColor(img_resized, cv2.COLOR_BGR2RGB))
    plt.title(f"{labels[class_id]} ({confidence:.2f}%)")
    plt.axis('off')
    plt.show()

