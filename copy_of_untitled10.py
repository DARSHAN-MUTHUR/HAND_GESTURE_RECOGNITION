import zipfile

zip_ref = zipfile.ZipFile('/content/hand_sign_dataset.zip', 'r')
zip_ref.extractall('/content')
zip_ref.close()

print("Extracted!")

import os
os.listdir('/content/hand_sign_dataset')

import tensorflow as tf
from tensorflow.keras.preprocessing.image import ImageDataGenerator

# Data generator
train_datagen = ImageDataGenerator(
    rescale=1./255,
    validation_split=0.2
)

train = train_datagen.flow_from_directory(
    '/content/hand_sign_dataset',
    target_size=(224,224),
    batch_size=16,
    class_mode='categorical',
    subset='training'
)

val = train_datagen.flow_from_directory(
    '/content/hand_sign_dataset',
    target_size=(224,224),
    batch_size=16,
    class_mode='categorical',
    subset='validation'
)

# Model
base_model = tf.keras.applications.MobileNetV2(
    input_shape=(224,224,3),
    include_top=False,
    weights='imagenet'
)

base_model.trainable = False

model = tf.keras.Sequential([
    base_model,
    tf.keras.layers.GlobalAveragePooling2D(),
    tf.keras.layers.Dense(128, activation='relu'),
    tf.keras.layers.Dense(train.num_classes, activation='softmax')
])

model.compile(
    optimizer='adam',
    loss='categorical_crossentropy',
    metrics=['accuracy']
)

# Train
history = model.fit(train, validation_data=val, epochs=5)

# Save model
model.save("hand_sign_model.h5")

